package java_example_180514;

public class VariableExample {
	//메인 메소드 선언
	public static void main(String[] args) {
		//변수 선언 
		//value라는 이름의 정수형 변수를 선언하면서 초기값을 10으로 할당함. 
		/*int value = 10;*/
		int value;
		value = 10;
		
		//value의 값에 10을 더해서 결과값을 result라는 변수에 담으세요. 
		int result;
		result = value+10;
		
		//result가 가지고 있는 값을 출력 
		System.out.println(result);
		System.out.print("result");
		System.out.println("따옴표 안의 값은 그대로 출력됨");
		//System.out.println();
		
	}

}
